# Datos de entrada

base = int(input('Digite la base del triángulo: '))
altura = int(input('Digite la altura del triángulo: '))

# proceso

area_triangulo = (base * altura) / 2

# Salida

print(f'El área del triángulo es: {area_triangulo:.2f}')


